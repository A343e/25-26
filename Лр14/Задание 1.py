class ТехОбслуживание:
    def __init__(self, название_станции, адрес_станции, название_автотранспорта, вид_ремонта, дата_поступления, дата_выдачи, результат_ремонта, фамилия_персонала, сумма_ремонта):
        self.название_станции = название_станции
        self.адрес_станции = адрес_станции
        self.название_автотранспорта = название_автотранспорта
        self.вид_ремонта = вид_ремонта
        self.дата_поступления = дата_поступления
        self.дата_выдачи = дата_выдачи
        self.результат_ремонта = результат_ремонта
        self.фамилия_персонала = фамилия_персонала
        self.сумма_ремонта = сумма_ремонта

